void U1BoundValue(int BdComp, double Param, double &value)
{
  double t = TDatabase::TimeDB->CURRENTTIME;

  switch(BdComp)
  {
    case 0:
    case 1:
    case 2:
    case 3:
    case 4:
      value=0;
      break;
    case 5: 
      value = Param*(2.0-Param);
      break;
    case 6:
      value =1;
      break;
    case 7: 
      value=(1-Param)*(1+Param);
      break;
    default: cout << "wrong boundary part number" << endl;
            break;
  }
}

